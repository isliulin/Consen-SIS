/*
***********************************************************************************************************************
* 函数名称: SysGetCurExeIndex
*
* 功能描述: 获取当前代码运行索引号。
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返 回 值  : 运行索引号
*
* 注意事项: 无
***********************************************************************************************************************
*/
excute_id SysGetCurExeIndex(void)
{
    excute_id emRetRunIndex = 0U;
    emRetRunIndex = s_emRetRunIndex;

    return emRetRunIndex;
}
/*
***********************************************************************************************************************
* 函数名称: SysSetCurExeIndex
*
* 功能描述: 设置当前代码运行索引号。
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返 回 值  : 运行索引号
*
* 注意事项: 无
***********************************************************************************************************************
*/
void SysSetCurExeIndex(excute_id emRunIndex)
{
    s_emRetRunIndex = emRunIndex % MAX_PROGRAMS;

    return;
}

