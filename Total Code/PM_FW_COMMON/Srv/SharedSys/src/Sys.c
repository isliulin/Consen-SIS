/*
***********************************************************************************************************************
* 函数名称: SysInit
*
* 功能描述: 系统资源管理模块初始化
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返 回 值  : 无
*
* 注意事项: 无
***********************************************************************************************************************
*/
void SharedSysInit(void)
{
    /* 0为core0,1为core1 */
    if(0==get_cpu_no())
    {
        LogInit();

        SharedMemInit();

        SysCore0RtDataInit();

        SysMonitorInit();
    }

    return;
}




