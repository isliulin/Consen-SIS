/*
***********************************************************************************************************************
* 函数名称: SysGetSyncTime
*
* 功能描述: 获取同步时钟值(百微秒)。
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返 回 值  : 无
*
* 注意事项: 无
***********************************************************************************************************************
*/
void SysGetSyncTime(SyncTime64_t* punSyncTime)
{
    uint16_t usHigh16 = 0;
    uint32_t uiLow32 = 0;

    if(punSyncTime != NULL)
    {
        usHigh16 = SysGetSyncTimeHigh16();
        punSyncTime->stTimeStruct.uiHigh32b = (uint32_t)usHigh16;

        uiLow32 = SysGetSyncTimeLow32();
        punSyncTime->stTimeStruct.uiLow32b = (uint32_t)uiLow32;
    }
    return;
}

