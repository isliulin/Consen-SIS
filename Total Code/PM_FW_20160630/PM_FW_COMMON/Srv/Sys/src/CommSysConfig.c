/*
*********************************函数描述******************************************************************************
* 函数名称: SysCheckCMSlotID
*
* 功能描述: 根据槽位号查找对应内部ID
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返 回 值: 无
*
* 注意事项:
***********************************************************************************************************************
*/
uint32_t SysCheckCMSlotID(uint16_t usCMSlotID,CMSlotIdTabl_t* pstCMSlotIdTabl)
{
    uint32_t uiCount = 0U;
    uint32_t uiRet = INVALID_CM_SLOT_NUM;

    for(uiCount = 0U; uiCount < pstCMSlotIdTabl->uiCMNum; uiCount++)
    {
        if(usCMSlotID == pstCMSlotIdTabl->uiCMSlotId[uiCount])
        {
            uiRet = uiCount;
            break;
        }
    }

    return uiRet;
}




