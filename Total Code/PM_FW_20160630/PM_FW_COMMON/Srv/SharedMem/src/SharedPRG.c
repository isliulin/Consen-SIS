/*
***********************************************************************************************************************
* 函数名称: SharedPRGInit
*
* 功能描述: 初始化
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返 回 值  : 无
*
* 注意事项: 无
***********************************************************************************************************************
*/
void SharedPRGInit(void)
{
    /* 初始化 */
    memset((puint8_t) &g_stSharedPRG, 0 , sizeof(SharedPRGArea_t));

    g_stSharedPRG.bLock = PRG_AREA_UNLOCKED;
    g_stSharedPRG.bStatus = PRG_AREA_WRITE_ONLY;
    return;
}

