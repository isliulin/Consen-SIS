void TskRTSTaskExeEntry(void)
{
    /* 开中断 */
    powerpcIntEnable();

    uiStartTime = SysGetSyncTimeLow32 ();

    if(((uiStartTime%TASK_PERIOD) >= uiTimeWindow1)&&((uiStartTime%TASK_PERIOD) <= uiTimeWindow2))
    {
        //printf("bin1\n");
        /* 执行RTS任务 */
        TskRTSTaskExe();

    }
    return;
}
