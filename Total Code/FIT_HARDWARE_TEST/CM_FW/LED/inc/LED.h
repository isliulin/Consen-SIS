/* 状态灯 */
typedef enum LEDNameTag
{
    LED_NAME_ACTIVE,                              /* ACTIVE灯 */
    LED_NAME_FAULT,                               /* FAULT灯 */
    LED_NAME_PASS,                                /* PASS灯 */
    LED_NAME_CMBUS,                               /* CM_BUS灯 */
    NUM_OF_LED,                                   /* 状态灯个数 */
    LED_NAME_ILLEGAL
} LEDName_t;

/* 状态 */
typedef enum LEDStateTag
{
    LED_OFF = 0,                                  /* 灭*/
    LED_ON,                                       /* 亮 */
    LED_BLINK,                                    /* 慢闪 */
    NUM_OF_LED_STATE,                             /* 状态 */
    LED_ILLEGAL_STATE                             /* 非法状态 */
} LEDState_t;

/*
***********************************************************************************************************************
*                                全局函数声明
***********************************************************************************************************************
*/

/*
***********************************************************************************************************************
* 函数名称: GetLEDState
*
* 功能描述: 应用程序通过该函数获取LED的状态
*
* 输入参数: emLED:相应的LED名称
*
* 输出参数: 无
*
* 返 回 值  : LED的状态
*
* 注意事项: 无
***********************************************************************************************************************
*/
LEDState_t GetLEDState(LEDName_t emLED);

/*
***********************************************************************************************************************
* 函数名称: SetLEDState
*
* 功能描述: 应用程序设置指定的LED的状态，LED的实际亮灭情况不发生改变，当应用程序调用UpdateLEDRunningState()时，其实际运行情况才发生改变
*
* 输入参数: emLED:相应的LED名称
*        emLEDState: 相应的LED的状态（亮，灭，闪...）
*
* 输出参数: 无
*
* 返 回 值  : 无
*
* 注意事项: 无
***********************************************************************************************************************
*/
void SetLEDState(LEDName_t emLED, LEDState_t emLEDState);

/*
***********************************************************************************************************************
* 函数名称: LEDManagerInit
*
* 功能描述: 状态灯管理初始化
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返 回 值  : 无
*
* 注意事项: 无
***********************************************************************************************************************
*/
void LEDManagerInit(void);

/*
***********************************************************************************************************************
* 函数名称: LEDManagerCycle
*
* 功能描述: 状态灯管理周期运行函数
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返 回 值  : 无
*
* 注意事项: 无
***********************************************************************************************************************
*/
void LEDManagerCycle(void);


