/*
***********************************************************************************************************************
* 函数名称: MonitorInit
*
* 功能描述: 初始化
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返 回 值  : 无
*
* 注意事项: 无
***********************************************************************************************************************
*/
void MonitorInit(void)
{
    /*初始化温度*/
    SysTemperatureInit();

    /*初始化电源电压报警位*/
    SysSupplyAlarmBitInit();

    /*初始化PM离在线状态*/
    SysPMHardStatusInit(PMA);
    SysPMHardStatusInit(PMB);
    SysPMHardStatusInit(PMC);
    
}

/*
***********************************************************************************************************************
* 函数名称: MonitorCycle
*
* 功能描述: 周期运行函数
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返 回 值  : 无
*
* 注意事项: 无
***********************************************************************************************************************
*/
void MonitorCycle(void)
{
//    printf("  MonitorCycle  \n");
    PMController_t emLocalPMId = SysGetLocalPMID();
    uint16_t usPMState = 0U;
    
    MonitorGetSysState();
    
    usPMState = SharedGetPMState();
    SysSetPMState(emLocalPMId, usPMState);

    return;
}




