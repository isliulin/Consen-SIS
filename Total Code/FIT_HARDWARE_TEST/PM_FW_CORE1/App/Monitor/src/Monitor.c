/*
***********************************************************************************************************************
* 函数名称: MonitorGetSysState
*
* 功能描述: 周期获取硬件寄存器内记录的系统状态
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返 回 值  : 无
*
* 注意事项: 无
***********************************************************************************************************************
*/
void MonitorGetSysState(void)
{
    PMController_t emLocalPMId = SysGetLocalPMID();                 /* 本PMID号           */
    PMController_t emNextPMId = SysGetNextPMID(emLocalPMId);        /* 下一个PMID号     */
    PMController_t emPrePMId = SysGetPrePMID(emLocalPMId);          /* 前一个PMID号     */

    /*从硬件寄存器获取温度，并写在系统资源*/
    SysReadTemperature();
    /*从硬件寄存器获取电源电压，并写在系统资源*/
    SysReadSupplyAlarmBit();
    /*从硬件寄存器获取本机PM id，并写在系统资源*/
//    SysReadLocalPMID();
    
    /*从硬件寄存器获取PM离在线状态，并写在系统资源*/
    SysReadPMHardStatus(emLocalPMId);
    SysReadPMHardStatus(emNextPMId);
    SysReadPMHardStatus(emPrePMId);

    SysReadPPMStat();
    SysReadMCUStat();

    return;
}






